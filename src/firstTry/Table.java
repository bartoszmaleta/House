package firstTry;

public class Table {
    private int plates;
    private String tableClothcolour;

    public Table(int plates, String tableClothcolour) {
        this.plates = plates;
        this.tableClothcolour = tableClothcolour;
    }

    public int getPlates() {
        return plates;
    }

    public void setPlates(int plates) {
        this.plates = plates;
    }

    public String getTableClothcolour() {
        return tableClothcolour;
    }

    public void setTableClothcolour(String tableClothcolour) {
        this.tableClothcolour = tableClothcolour;
    }

    
    
}